public class DoubleArithmeticTest {
    public static void main(String[] args) {
        double a = 12345.6789;
        double b = 9876.5432;

        System.out.println(a + b); // dadd
        System.out.println(a - b); // dsub
        System.out.println(a * b); // dmul
        System.out.println(a / b); // ddiv
    }
}
