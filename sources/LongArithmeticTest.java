public class LongArithmeticTest {
    public static void main(String[] args) {
        long a = 5000000000L;
        long b = 3000000000L;

        System.out.println(a + b); // ladd
        System.out.println(a - b); // lsub
        System.out.println(a * b); // lmul
        System.out.println(a / b); // ldiv
    }
}
