public class MaxTest {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int max = Math.max(a, b);
        System.out.println("The maximum of " + a + " and " + b + " is " + max);

        int c = -5;
        int d = -10;
        int max2 = Math.max(c, d);
        System.out.println("The maximum of " + c + " and " + d + " is " + max2);
    }
}