public class FloatArithmeticTest {
    public static void main(String[] args) {
        float a = 12.5f;
        float b = 3.5f;

        System.out.println(a + b); // fadd
        System.out.println(a - b); // fsub
        System.out.println(a * b); // fmul
        System.out.println(a / b); // fdiv
    }
}
